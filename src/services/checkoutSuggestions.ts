/**
 * Récupère la suggestion de finition (checkout) pour un score donné et un mode de jeu.
 * @param score Le score restant du joueur.
 * @param doubleOut Si true, utilise les règles de Double Out.
 * @returns Un tableau de chaînes représentant les fléchettes à viser, ou null si aucune suggestion.
 */
export function getCheckoutSuggestion(score: number, doubleOut: boolean): string[] | null {
  if (score <= 0 || score > 180) return null;
  const suggestions = doubleOut ? DOUBLE_OUT_CHECKOUTS : NORMAL_OUT_CHECKOUTS;
  return suggestions[score] || null;
}
